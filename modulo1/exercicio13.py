# Exercício 13 – Operadores Lógicos e de Comparação

a = 120
b = 45

print("Números:", a, "e", b)
print()
print("1. O primeiro é maior que o segundo?", a > b)
print("2. Os dois são iguais?              ", a == b)
print("3. Ambos são positivos?             ", a > 0 and b > 0)
print("4. Pelo menos um é maior que 100?   ", a > 100 or b > 100)
print("5. O primeiro é diferente de zero?  ", a != 0)